package task.performance.vo;

public class PerformancePicture {

  public int no;
  public String picture_name;
  public String picture_code;
  public int performance_no;

  public int getNo() {
    return no;
  }

  public void setNo(int no) {
    this.no = no;
  }

  public String getPicture_name() {
    return picture_name;
  }

  public void setPicture_name(String picture_name) {
    this.picture_name = picture_name;
  }

  public String getPicture_code() {
    return picture_code;
  }

  public void setPicture_code(String picture_code) {
    this.picture_code = picture_code;
  }

  public int getPerformance_no() {
    return performance_no;
  }

  public void setPerformance_no(int performance_no) {
    this.performance_no = performance_no;
  }
}
